package control_Statements;

public class PositiveNegativeZero {
    public static void main(String[] args) {
        int num=0;
        if (num>0){
            System.out.println("number is positive");
        }else if (num<0){
            System.out.println("number is negative");
        }else{
            System.out.println("number is 0");
        }
    }
}
